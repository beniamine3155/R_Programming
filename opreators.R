a = 10
b = 3

#Arithmetic Operator
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b)
print(a%/%b)
print(a^b)

#Relational Operator
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

#Logical Operator
print(a && b)
print(a || b)

vec1 <- c(1,2,-3,45,0)
vec2 <- c(12,0,33,4,25)

print(vec1 & vec2)
print(vec1 | vec2)
print(!vec1)

#Assignment Operator
a = 12
b <- 23
cc <<- 55

12 -> e
55 ->> f

print(a)
print(b)
print(cc)
print(e)
print(f)

#Misc. Operator
xx <- 5:20
print(xx)

print(10 %in% xx)
print(30 %*% xx)



